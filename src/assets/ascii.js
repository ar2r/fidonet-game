export const DOWNLOAD_PROGRESS = (filename, percent) => {
    const total = 30;
    const filled = Math.floor(total * percent / 100);
    const empty = total - filled;
    return `Скачивание ${filename}: [${'█'.repeat(filled)}${'░'.repeat(empty)}] ${percent}%`;
};
